// КАК ОТЛИЧИТЬ ИНСТРУКЦИЮ ОТ ВЫРАЖЕНИЯ
// Выражения могут быть исользованы как аргументы в вызовах функций
// Инструкции НЕ могут быть исользованы как аргументы в вызовах функций

function checkFn(a) {
  console.log(a);
}

const i = true;
let p = 10;

