// Если выражение возвращает значение, то ИНСТРУКЦИЯ ВЫПОЛНЯЕТ ДЕЙСТВИЯ
// Инструкция пишется на отдельной строке
// Инструкция обычно заканчивается точкой с запятой
// Исключение: Точка с запятой не требуется после блока инструкций
// Точку с запятой можно опускать

// Примеры инструкций (let, const, if, for, ...)
let n;

const k = 10;
const m = 5;

if (k > m) {
  console.log("k is larger");
}

for (let i = 0; i < 5; i += 1) {
  console.log(i);
}

